function ItemNormal_AddBuff(obj, idx, num, count, max_num)
  local now = os.time()
  local num2 = obj:getBuffData(idx);
  if num2 > now then
    num2 = num2 - now
    if num2 >= max_num then
      return false
    end
  else
    num2 = 0
  end
  num2 = num2 + num * count
  if num2 >= max_num then
    num2 = max_num
  end
  obj:setBuffData(idx, num2 + now, true)
  return true
end

